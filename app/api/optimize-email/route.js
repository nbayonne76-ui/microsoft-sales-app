export async function POST(request) {
  try {
    const { content, tone, language = 'french' } = await request.json();
    
    console.log('🎯 API Debug - Tone reçu:', tone);
    console.log('🚨 API CALLED FROM:', request.headers.get('referer'));
    
    if (!content || !content.trim()) {
      return Response.json({ error: 'Content is required' }, { status: 400 });
    }

    // Sélectionner un template variant selon le ton
    const variants = TEMPLATE_VARIANTS[tone] || TEMPLATE_VARIANTS.professional;
    
    // Choisir aléatoirement parmi les variants pour créer de la variation
    const randomIndex = Math.floor(Math.random() * variants.length);
    const selectedTemplate = variants[randomIndex];
    
    // Construire l'email optimisé
    const optimizedContent = `Objet: ${selectedTemplate.subject}

${selectedTemplate.content}`;

    console.log('✅ Transformation terminée pour ton:', tone);
    console.log('📝 Longueur contenu original:', content.length);
    console.log('📝 Longueur contenu optimisé:', optimizedContent.length);
    console.log('🎨 Différence détectée:', content !== optimizedContent);

    // Track email generation dans analytics
    try {
      const trackResponse = await fetch(`${process.env.NEXTAUTH_URL || 'http://localhost:3008'}/api/analytics`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          action: 'track_email',
          data: {
            tone,
            templateIndex: randomIndex,
            subject: selectedTemplate.subject,
            contentLength: optimizedContent.length
          }
        })
      });
      
      const trackResult = await trackResponse.json();
      console.log('📊 Analytics tracked:', trackResult.emailId);

      return Response.json({
        optimizedContent,
        originalLength: content.length,
        optimizedLength: optimizedContent.length,
        tone,
        templateIndex: randomIndex,
        emailId: trackResult.emailId, // Pour le feedback plus tard
        improvements: [
          `Adaptation du ton ${tone}`,
          'Optimisation de la structure',
          'Amélioration de la force commerciale'
        ]
      });
    } catch (trackError) {
      console.warn('⚠️ Analytics tracking failed:', trackError.message);
      
      return Response.json({
        optimizedContent,
        originalLength: content.length,
        optimizedLength: optimizedContent.length,
        tone,
        templateIndex: randomIndex,
        improvements: [
          `Adaptation du ton ${tone}`,
          'Optimisation de la structure',
          'Amélioration de la force commerciale'
        ]
      });
    }

  } catch (error) {
    console.error('❌ Error in email optimization:', error);
    return Response.json({ 
      error: 'Failed to optimize email',
      details: error.message 
    }, { status: 500 });
  }
}