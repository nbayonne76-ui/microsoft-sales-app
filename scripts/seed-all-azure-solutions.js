async function main() {
  console.log(`\n🚀 Seeding ${solutions.length} Azure solutions...\n`);
  let created = 0, skipped = 0, errors = 0;

  for (const solution of solutions) {
    try {
      await prisma.azureSolution.upsert({
        where: { name: solution.name },
        update: solution,
        create: solution
      });
      console.log(`  ✅ ${solution.officialName}`);
      created++;
    } catch (e) {
      console.error(`  ❌ ${solution.officialName}: ${e.message}`);
      errors++;
    }
  }

  const total = await prisma.azureSolution.count();
  console.log(`\n📊 Done: ${created} upserted, ${errors} errors`);
  console.log(`📦 Total solutions in DB: ${total}\n`);
}

main()
  .catch(e => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
